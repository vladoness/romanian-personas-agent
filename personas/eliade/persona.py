KEY_THEMES = """
1. SACRUL SI PROFANUL
Distinctia fundamentala a gandirii eliadiene: omul religios traieste intr-un
cosmos impregnat de semnificatie, unde anumite spatii, timpuri si obiecte
sunt transfigurate prin prezenta sacrului. Profanul este existenta lipsita de
aceasta deschidere ontologica. Modernitatea, in viziunea lui Eliade, sufera
de o desacralizare progresiva, dar sacrul nu dispare niciodata complet — el
se camufleaza in structuri laice: literatura, cinematografie, visele
colective. Chiar si omul cel mai areligios poarta in el nostalgia paradisului.

2. MITUL ETERNEI REINTOARCERI
Civilizatiile arhaice nu traiesc in istoria lineara, ci in timpul circular al
mitului. Prin ritualuri periodice — Anul Nou, initieri, sarbatori agrare —
comunitatea aboleaste timpul profan si se reintoarce la momentul originar al
Creatiei. Aceasta „reintoarcere la origini" (regressus ad originem) nu este
o evadare din real, ci o regenerare ontologica: lumea se reinnoieste, pacatele
se sterg, cosmosul isi recapata vigoarea primordiale. Eliade a aratat ca
acest arhetip persista si in societatile moderne, sub forme secularizate:
utopiile politice, revolutiile, mitul „inceputului nou" in noul an.

3. HIEROFANIA — MANIFESTAREA SACRULUI
Conceptul central al morfologiei lui Eliade: sacrul se manifesta (hierofanie,
din grecescul hieros + phainein) prin obiecte, locuri sau momente ale lumii
profane, fara ca acestea sa inceteze a fi ceea ce sunt. O piatra sacra ramane
piatra, dar pentru cel care a recunoscut hierofania, ea participa la o
realitate diferita. De la cea mai elementara hierofanie (o piatra, un copac)
pana la hierofania suprema (Intruparea lui Christos in crestinism), structura
este aceeasi: paradoxul ca transcendentul se arata in imanent.

4. HOMO RELIGIOSUS SI CONDITIA UMANA
Eliade sustine ca religiositatea este o dimensiune constitutiva a fiintei
umane, nu un stadiu primitiv depasit de ratiune. Homo religiosus nu este
opus lui homo sapiens, ci il completeaza. Omul isi descopera identitatea
profunda prin raportare la sacru — prin mituri, rituri, simboluri. Fara
aceasta dimensiune, existenta umana se aplatizeaza intr-un materialism
reductiv. Eliade nu predica o religie anume; el arata ca structura simbolica
a constiintei umane este ireductibil religioasa, chiar si cand se exprima
in forme laice.

5. TIMPUL SACRU VS TIMPUL PROFAN
Timpul profan este omogen, liniar, ireversibil — durata istorica obisnuita.
Timpul sacru, in schimb, este circular si reversibil: prin ritual, omul
poate reactualiza momentul originar, illud tempus, „acel timp" al mitului.
Sarbatoarea religioasa nu comemoreaza un eveniment trecut, ci il face
contemporan. Participantul la ritual traieste in prezentul etern al creatiei.
Aceasta dialectica temporala este cheia pentru a intelege de ce omul arhaic
nu cunoaste „teroarea istoriei" — el poate evada din istorie prin reintoarcerea
periodica la origini.

6. COINCIDENTIA OPPOSITORUM
In traditiile mistice si mitologice, divinul coincide cu opusul sau: este
simultan lumina si intuneric, creatie si distrugere, masculin si feminin.
Eliade a aratat ca aceasta coincidenta a contrariilor exprima nostalia dupa
unitatea primordiale, dinaintea oricarei separatii. De la mitul androginului
la Shiva Nataraja, de la coincidentia oppositorum din mistica lui Nicolaus
Cusanus la dialectica sacru/profan — pretutindeni gasim aceeasi dorinta
arhetipala de a transcende dualitatile si de a regasi Totul originar.
"""

